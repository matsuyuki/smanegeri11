<?php

namespace SMA\SMABundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Berita
 */
class Berita
{
    /**
     * @var string
     */
    private $judul;

    /**
     * @var string
     */
    private $isi;

    /**
     * @var \DateTime
     */
    private $tanggal;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set judul
     *
     * @param string $judul
     * @return Berita
     */
    public function setJudul($judul)
    {
        $this->judul = $judul;

        return $this;
    }

    /**
     * Get judul
     *
     * @return string 
     */
    public function getJudul()
    {
        return $this->judul;
    }

    /**
     * Set isi
     *
     * @param string $isi
     * @return Berita
     */
    public function setIsi($isi)
    {
        $this->isi = $isi;

        return $this;
    }

    /**
     * Get isi
     *
     * @return string 
     */
    public function getIsi()
    {
        return $this->isi;
    }

    /**
     * Set tanggal
     *
     * @param \DateTime $tanggal
     * @return Berita
     */
    public function setTanggal($tanggal)
    {
        $this->tanggal = $tanggal;

        return $this;
    }

    /**
     * Get tanggal
     *
     * @return \DateTime 
     */
    public function getTanggal()
    {
        return $this->tanggal;
    }

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }
}
